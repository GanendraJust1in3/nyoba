    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            koneksi();
            int row =tabel.getSelectedRow();
            String tabel_klik=(tabel.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from data_jabatan where JABATAN ='"+tabel_klik+"'");
            if(sql.next()){
                String JABATAN = sql.getString("JABATAN");
                jabatan.setText(JABATAN);
            }
        } catch (SQLException e) {}
    }//GEN-LAST:event_tabelMouseClicked

