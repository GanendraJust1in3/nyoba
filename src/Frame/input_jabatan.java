private void Clear(){
    jabatan.setText(null);
}
     
private void tabel(){
   DefaultTableModel tb= new DefaultTableModel();
   // Memberi nama pada setiap kolom tabel
   tb.addColumn("JABATAN");
   tabel.setModel(tb);
       try{
       // Mengambil data dari database
       res=stat.executeQuery("select * from data_jabatan");

       while (res.next()){
           // Mengambil data dari database berdasarkan nama kolom pada tabel
           // Lalu di tampilkan ke dalam JTable
           tb.addRow(new Object[]{
           res.getString("JABATAN")
           });
       }

   }catch (Exception e){
   }
} 

private void simpan(){
    try{
        koneksi();
        statLgn = con.createStatement();
        String SQL = "insert into data_jabatan values('0','"+jabatan.getText()+"')";
        statLgn.executeUpdate(SQL);
        tabel();
        statLgn.close();
        con.close();
        Clear();
        JOptionPane.showMessageDialog(null, "Berhasil Memasukkan");

      }catch(SQLException exc){
          System.err.println(exc.getMessage());}                                   
}
    
private void edit(){
    try{
        koneksi();
        statLgn = con.createStatement();
        String SQL = "UPDATE `data_jabatan` SET `JABATAN`='"+jabatan.getText()+"' WHERE JABATAN ='"+jabatan.getText()+"'";
        statLgn.executeUpdate(SQL);
        tabel();
        statLgn.close();
        con.close();
        Clear();
        JOptionPane.showMessageDialog(null, "Berhasil Merubah");
           
    }catch(Exception exc){
        System.err.println(exc.getMessage());}
}

private void hapus(){
    try{
        koneksi();
        statLgn = con.createStatement();
        String SQL = "DELETE FROM data_jabatan WHERE JABATAN = '"+jabatan.getText()+"'";
        statLgn.executeUpdate(SQL);
        tabel();
        statLgn.close();
        con.close();
        Clear();
        JOptionPane.showMessageDialog(null, "Berhasil Menghapus");
           
    }catch(Exception exc){
        System.err.println(exc.getMessage());}
}
